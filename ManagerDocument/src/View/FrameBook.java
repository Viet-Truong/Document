    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = tableBook.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(FrameBook.this, "vui long chon sach muon xoa", "loi", JOptionPane.ERROR_MESSAGE);
        }else{
            int confirm = JOptionPane.showConfirmDialog(FrameBook.this, "Ban co chac chan muon xoa");
            
            if(confirm == JOptionPane.YES_OPTION){
                int bookId = Integer.valueOf(String.valueOf(tableBook.getValueAt(row, 0)));
                try {
                    bookService.deleteBook(bookId);
                } catch (SQLException ex) {
                    Logger.getLogger(FrameBook.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            try {
                defaultTableModel.setRowCount(0);
                setTableData(bookService.getAllBooks());
            } catch (SQLException ex) {
                Logger.getLogger(FrameBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

