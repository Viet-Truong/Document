    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:
        int row = tableBook.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(FrameBook.this, "vui long chon sach muon sua", "loi", JOptionPane.ERROR_MESSAGE);
        }else{
            int bookID = Integer.valueOf(String.valueOf(tableBook.getValueAt(row, 0)));
            try {
                new UpdateBook(bookID).setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(FrameBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        } 
    }//GEN-LAST:event_UpdateBtnActionPerformed

    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        // TODO add your handling code here:
        Book s = new Book();
        s.setNameNXB(nameNXBfind.getText());
        try {
            s = bookService.findBook(s);
        } catch (SQLException ex) {
            Logger.getLogger(FrameBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (s != null) {
            defaultTableModel.setRowCount(0);
            defaultTableModel.addRow(new Object[]{
                s.getIdTL(),
                s.getNameNXB(),
                s.getNameAuthor(),
                s.getSoBanPhatHanh(),
                s.getPage()
            });
        } else {
            defaultTableModel.setNumRows(0);
            JOptionPane.showMessageDialog(null, "Không Có Trong Data");
        }
    }//GEN-LAST:event_findBtnActionPerformed
    private void setTableData(List<Book> books){
        for(Book book: books){
            defaultTableModel.addRow(new Object[]{book.getIdTL(), book.getNameNXB(), book.getNameAuthor(),
                book.getSoBanPhatHanh(), book.getPage()});
        }
    }
